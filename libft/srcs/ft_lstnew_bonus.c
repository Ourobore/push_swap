#include "../libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*new;

	new = malloc(sizeof(t_list));
	if (!new)
		return (NULL);
	if (!content)
		new->content = NULL;
	new->content = malloc(sizeof(void *));
	if (!new->content)
		return (NULL);
	new->content = content;
	new->next = NULL;
	return (new);
}
